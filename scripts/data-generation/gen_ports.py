import json

with open("/home/claude/scripts/countries_generated.json", encoding="utf-8") as f:
    countries = json.load(f)
country_by_iso = {c["isoCode"]: c for c in countries}

ALL = TIER1 + TIER1_EXTRA + TIER2 + TIER3 + TIER4

seen_unlocode = set()
rows = []
dup_count = 0
missing_country = set()
for iso2, name, unlocode, lat, lon, roro, container, verified in ALL:
    if unlocode in seen_unlocode:
        dup_count += 1
        continue
    seen_unlocode.add(unlocode)
    country = country_by_iso.get(iso2)
    if not country:
        missing_country.add(iso2)
        continue
    port_id = f"{iso2.lower()}-{unlocode[2:].lower()}"
    rows.append({
        "id": port_id,
        "name": name,
        "unlocode": unlocode,
        "countryId": iso2.lower(),
        "countryName": country["name"],
        "latitude": lat,
        "longitude": lon,
        "nearbyAirports": [],
        "nearbyWarehouses": [],
        "roroAvailable": roro,
        "containerAvailable": container,
        "dataVerified": verified,
    })

print("Total raw entries:", len(ALL))
print("Duplicate UNLOCODEs skipped:", dup_count)
print("Missing country refs:", missing_country)
print("Final port count:", len(rows))
print("Countries with at least 1 port:", len(set(r["countryId"] for r in rows)))
verified_count = sum(1 for r in rows if r["dataVerified"])
print("Verified:", verified_count, "Unverified:", len(rows) - verified_count)

with open("/home/claude/scripts/ports_generated.json", "w", encoding="utf-8") as f:
    json.dump(rows, f, ensure_ascii=False, indent=2)
